// ─── Helper ─────────────────────────────────────────────────────

export function getRequirementsForMarketAndType(
  gatewayCode: string,
  marketCode: string,
  projectType: ProjectType
): GatewayReferenceRequirement[] {
  const gw = gatewayReference.find((g) => g.code === gatewayCode);
  if (!gw) return [];
  return gw.requirements.filter((r) => {
    const matchType = r.applicableProjectTypes.includes(projectType);
    const matchMarket =
      marketCode === 'Global' ||
      r.jurisdictions.length === 0 ||
      r.jurisdictions.includes(marketCode);
    return matchType && matchMarket;
  });
}

